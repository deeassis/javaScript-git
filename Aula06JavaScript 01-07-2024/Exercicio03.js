// Definindo a tabuada que queremos calcular
let tabuada = 4;

// Loop utilizando um for para calcular e exibir a tabuada
for (let i = 1; i <= 10; i++) {
        // Calculando o resultado da multiplicação
    let resultado = tabuada * i;
    // Exibindo a tabuada no formato "tabuada x i = resultado"
    console.log(`${tabuada} x ${i} = ${resultado}`);
}
